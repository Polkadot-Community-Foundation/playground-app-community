// Async clipboard first; legacy execCommand fallback for webviews that
// haven't wired navigator.clipboard through their permission layer (the
// Android host gates clipboard as a DeviceCapability — the web API may not
// reach it, but execCommand from a user gesture does).
export async function copyText(text: string): Promise<boolean> {
    try {
        await navigator.clipboard.writeText(text);
        return true;
    } catch {
        try {
            const ta = document.createElement("textarea");
            ta.value = text;
            ta.style.position = "fixed";
            ta.style.opacity = "0";
            document.body.appendChild(ta);
            ta.select();
            const ok = document.execCommand("copy");
            ta.remove();
            return ok;
        } catch {
            return false;
        }
    }
}
